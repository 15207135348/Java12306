package com.yy.domain;

public class JsFunc {

    private String name;
    private String total;

    public JsFunc(String name, String total) {
        this.name = name;
        this.total = total;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }

    @Override
    public String toString() {
        return total;
    }
}
